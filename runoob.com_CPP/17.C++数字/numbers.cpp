#include <iostream>
using namespace std;

int main()
{
    //数字定义和赋值
    short s = 10;
    int i = 1000;
    long l = 1000000;
    float f = 230.47;
    double d = 30949.374;

    cout << "short  s :" << s << endl;
    cout << "int    i :" << i << endl;
    cout << "long   l :" << l << endl;
    cout << "float  f :" << f << endl;
    cout << "double d :" << d << endl;

    return 0;
}
